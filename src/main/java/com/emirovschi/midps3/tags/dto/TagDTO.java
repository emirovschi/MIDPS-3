package com.emirovschi.midps3.tags.dto;

public class TagDTO
{
    private String name;
    private long count;

    public String getName()
    {
        return name;
    }

    public void setName(final String name)
    {
        this.name = name;
    }

    public long getCount()
    {
        return count;
    }

    public void setCount(final long count)
    {
        this.count = count;
    }
}
